package com.news.pojo;


public class Englishword {

  private long id;
  private String text;
  private long vip;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public String getText() {
    return text;
  }

  public void setText(String text) {
    this.text = text;
  }


  public long getVip() {
    return vip;
  }

  public void setVip(long vip) {
    this.vip = vip;
  }

}
